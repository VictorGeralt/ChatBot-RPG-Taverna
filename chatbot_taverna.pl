    format('\t6 - Pegar um trabalho'),nl,
    format('\t7 - Sair pela porta'),nl,
    format('\t8 - Ir para o quarto'),nl,
    format('-> '),
    read(Input),
    process_menu(Input),
	menu.

    process_menu(1):-
    skip_line,
    pedir_comida.

    process_menu(2):-
    skip_line,
    pedir_bebida.
    
    process_menu(3):-
    skip_line,
    alugar_quarto.
    
